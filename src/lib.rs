use crate::model::{build_and_load_model, Model, NDBackend};
use burn::tensor::Tensor;
use worker::*;

#[durable_object]
pub struct ModelRunner {
    model: Model<NDBackend>,
    state: State,
    env: Env, // access `Env` across requests, use inside `fetch`
}

#[durable_object]
impl DurableObject for ModelRunner {
    fn new(state: State, env: Env) -> Self {
        let model = Some(build_and_load_model());

        let model = match model.as_ref() {
            Some(model) => model,
            None => unimplemented!(),
        };
        Self {
            model: model.clone(),
            state: state,
            env,
        }
    }

    async fn fetch(&mut self, _req: Request) -> Result<Response> {
        // do some work when a worker makes a request to this DO
        let device = Default::default();
        let input: &[f32] = &TEST_4;
        let input = Tensor::<NDBackend, 1>::from_floats(input, &device).reshape([1, 28, 28]);
        let output = self.model.forward(input);

        let output = burn::tensor::activation::softmax(output, 1);
        let max_index = output.argmax(1);

        let result = max_index.into_scalar();
        Response::ok(&format!("{} found!", result))
    }
}

#[event(fetch)]
async fn fetch(req: Request, env: Env, _ctx: Context) -> Result<Response> {
    console_error_panic_hook::set_once();

    let model_runner = env.durable_object("MODEL")?.id_from_name("MY_MODEL_RUNNER")?.get_stub()?;
    // Response::ok(format!("Answer {result}!"))
    model_runner.fetch_with_request(req).await
}

